      int idAutor(String naziv)
    {
        String[] splited = naziv.split("\\s+");

        int id = 0;
        try {
            String queryString = "SELECT autor_id from autori where autori.ime = '"+splited[0]+"' AND autori.prezime = '"+splited[1]+"'";
            Statement smt = conn.createStatement();
            ResultSet resultSet = smt.executeQuery(queryString);
            if(resultSet.next())
                id = resultSet.getInt("autor_id");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        return id;
    }
      
