    private void setTableData() {
        try {
            int rows = 0;
            int rowIndex = 0;
            
            Object objAutori = cbAutor.getSelectedItem();
            autor_id = objAutori.toString();
            
            Statement smt = conn.createStatement();
            /*ResultSet rs = smt.executeQuery("SELECT rad_recezent.rad_recezent_id, IF(SUM(rad_recezent.odluka = 1) > 2, radovi.rb_sveske, 'Nije prihvacen') , "
                    + "radovi.naziv, rad_recezent.datum_slanja_recenzije, rad_recezent.datum_dobijanja_recenzije, "
                    + "rad_recezent.odluka FROM radovi INNER JOIN rad_recezent ON "
                    + "radovi.rad_id = rad_recezent.rad_id WHERE rad_recezent.recezent_id ="+idRecezent(recezent_id));*/
            ResultSet rs = smt.executeQuery("SELECT autori_radovi.autori_radovi_id, autori_radovi.rad_id,"
                    + "radovi.naziv FROM radovi INNER JOIN autori_radovi ON "
                    + "radovi.rad_id = autori_radovi.rad_id WHERE autori_radovi.autor_id ="+idAutor(autor_id));
            if (rs.next()) {
                rs.last();
                rows = rs.getRow();
                rs.beforeFirst();
            }
            
            String[][] data = new String[rows][3];
            while(rs.next())
            {
                data[rowIndex][0] = rs.getInt(1)+"";
                data[rowIndex][1] = rs.getString(3);
                data[rowIndex][2] = String.valueOf(brojOdobrenih(rs.getInt(2)));
                rowIndex++;
            }
            String[] cols = {"ID", "Rad", "Status"};
            
            DefaultTableModel model = new DefaultTableModel(data, cols);
            spisakRadovaAutora.setModel(model);
            
            rs.close();
            smt.close();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ne mogu se popuniti podaci..."+ex.getMessage());
        }
    }
    
